from .models import Profile, Image
from .serializers import ProfileeSerializer
import json
import logging


logger = logging.getLogger(__name__)


class UserLogoutAPIView(APIView):
    """
        View для выхода пользователя из системы
    """
    permission_classes = [IsAuthenticated]


    def post(self, request) -> Response:
        logout(request)
        logger.info('The user is logged out.')
        return Response({'message':'Вы вышли из системы!'})



class ProfileView(APIView):
    permission_classes = [IsAuthenticated]
    """
        View для перехода в профиль пользователя
    """

    def post(self, request) -> Response:

        profile = get_object_or_404(Profile, user=request.user)
        serializer = ProfileeSerializer(
            profile,
            data=request.data,
            partial=True,
        )

        if serializer.is_valid():
            serializer.save()
            logger.info('The user changed the profile information.')
            return Response(serializer.data)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)


    def get(self, request) -> Response:
        profile = get_object_or_404(Profile, user=request.user)
        serializer = ProfileeSerializer(profile)
        return Response(serializer.data)



class UserLoginView(APIView):
    """
        View для входа пользователя в систему
    """
    def post(self, request) -> Response:
        data = request.data

        username = None
        password = None

        if len(data) == 1:
            try:
                raw_key = list(data.keys())[0]
                parsed = json.loads(raw_key)

                username = parsed.get('username')
                password = parsed.get('password')
            except (json.JSONDecodeError, IndexError):
                pass
        else:
            username = data.get('username')
            password = data.get('password')

        if not username or not password:
            return Response(
                    {'error': 'Username or password not found'},
                    status=status.HTTP_400_BAD_REQUEST
                )

        user = authenticate(username=username, password=password)

        if user:
            login(request, user)
            logger.info('The user is logged in.')
            return Response(status=status.HTTP_200_OK)

        return Response({'error': 'Invalid credentials'}, status=400)





class UserRegisterView(APIView):

    """
        View для регистрации рользователя
    """
    def post(self, request) -> Response:
        data = request.data

        name = None
        username = None
        password = None

        if len(data) == 1:
            try:
                raw_key = list(data.keys())[0]
                parsed = json.loads(raw_key)

                name = parsed.get('name')
                username = parsed.get('username')
                password = parsed.get('password')
            except (json.JSONDecodeError, IndexError):
                pass
        else:
            name = data.get('name')
            username = data.get('username')
            password = data.get('password')

        if not name or not username or not password:
            return Response(
                    {'error': 'name, username and password are required'},
                    status=status.HTTP_400_BAD_REQUEST
                )

        if User.objects.filter(username=username).exists():
            return Response(
                    {'error': 'User already exists'},
                    status=status.HTTP_400_BAD_REQUEST
                )

        user = User.objects.create_user(
                username=username,
                password=password,
                first_name=name
            )

        Profile.objects.create(
                user=request.user,
                fullName=username,
                email='',
                avatar='',
                phone=''
            )

        login(request, user)
        logger.info('The user has registered')
        return Response(status=status.HTTP_201_CREATED)




class ChangePasswordView(APIView):
    """
        View для изменения пароля пользователя
    """

    def post(self, request) -> Response:
        if not request.user.is_authenticated:
            return Response(
                {'error': 'Authentication required'},
                status=status.HTTP_401_UNAUTHORIZED
            )

        data = request.data

        old_password = None
        new_password = None

        if len(data) == 1:
            try:
                raw_key = list(data.keys())[0]
                parsed = json.loads(raw_key)

                old_password = parsed.get('currentPassword')
                new_password = parsed.get('newPassword')
            except (json.JSONDecodeError, IndexError):
                pass
        else:
            old_password = data.get('currentPassword')
            new_password = data.get('newPassword')

        if not old_password or not new_password:
            return Response(
                {'error': 'Not password'},
                status=status.HTTP_400_BAD_REQUEST
            )

        user = request.user

        if not user.check_password(old_password):
            return Response(
                {'error': 'Old password is incorrect'},
                status=status.HTTP_400_BAD_REQUEST
            )

        user.set_password(new_password)
        user.save()

        update_session_auth_hash(request, user)

        return Response(status=status.HTTP_200_OK)





class ChangeAvatarView(APIView):
    """
        View для изменения аватара пользователя
    """
    parser_classes = [MultiPartParser, FormParser]

    def post(self, request) -> Response:
        if not request.user.is_authenticated:
            return Response(
                {'error': 'Authentication required'},
                status=status.HTTP_401_UNAUTHORIZED
            )

        avatar_file = request.FILES.get('avatar')

        if not avatar_file:
            return Response(
                {'error': 'Avatar file is required'},
                status=status.HTTP_400_BAD_REQUEST
            )

        profile = get_object_or_404(Profile, user=request.user)


        if profile.avatar:
            profile.avatar.delete()

        image = Image.objects.create(
            src=avatar_file,
            alt=f"{profile.user.username} avatar"
        )

        profile.avatar = image
        profile.save()

        return Response(status=status.HTTP_200_OK)